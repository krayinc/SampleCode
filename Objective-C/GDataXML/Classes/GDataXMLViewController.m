- (void)viewDidLoad {
  [super viewDidLoad];

  // Uncomment the following line to display an Edit button in the navigation bar for this view controller.
  // self.navigationItem.rightBarButtonItem = self.editButtonItem;
  
  NSData *xmlData = [[NSData alloc] initWithContentsOfFile:[[NSBundle mainBundle] pathForResource:@"Members"
                                                                                           ofType:@"xml"]];
  NSError *error;
  // 1. XML をパース
  GDataXMLDocument *doc = [[GDataXMLDocument alloc] initWithData:xmlData
                                                         options:0
                                                           error:&error];
  [xmlData release];
  if (error != nil) { NSLog(@"%@", error); }

  // 2. XPath で該当のノードを抽出
  members = [[NSMutableArray alloc] init];
  NSArray *nodes = [doc nodesForXPath:@"/members/member" error:nil];

  for (GDataXMLElement *node in nodes) {
    // 3. 各ノードから ID 属性とテキストノードを抽出
    [members addObject:[NSDictionary dictionaryWithObjectsAndKeys:[[node attributeForName:@"id"] stringValue], @"id", [node stringValue], @"name", nil]];
  }

  [doc release];
}

